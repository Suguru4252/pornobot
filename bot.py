# 1. СИСТЕМА ОЧКОВ И УРОВНЕЙ
def add_points(chat_id, points=1):
    """Добавляет очки пользователю и проверяет повышение уровня"""
    if chat_id not in user_points:
        user_points[chat_id] = 0
        user_level[chat_id] = 1
    
    user_points[chat_id] += points
    
    # Расчет уровня (каждые 100 очков = +1 уровень)
    new_level = (user_points[chat_id] // 100) + 1
    if new_level > user_level.get(chat_id, 1):
        user_level[chat_id] = new_level
        return True  # Повышение уровня
    return False

# 2. ТАЙМЕРЫ
def set_timer(chat_id, minutes, message):
    """Устанавливает таймер"""
    def timer_callback():
        bot.send_message(chat_id, f"⏰ Таймер! {message}")
        if chat_id in user_timers:
            del user_timers[chat_id]
    
    timer = threading.Timer(minutes * 60, timer_callback)
    timer.daemon = True
    timer.start()
    user_timers[chat_id] = timer
    return timer

